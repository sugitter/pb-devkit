use std::io::Write;
use std::path::PathBuf;
use std::fs;

// ── Helpers ────────────────────────────────────────────────────────────

/// Create a temporary directory that is cleaned up on Drop.
struct TempDir {
    path: PathBuf,
}

impl TempDir {
    fn new(prefix: &str) -> Self {
        let dir = std::env::temp_dir().join(format!("pbdevkit_test_{}", prefix));
        let _ = fs::remove_dir_all(&dir);
        fs::create_dir_all(&dir).expect("Failed to create temp dir");
        TempDir { path: dir }
    }

    fn join(&self, name: &str) -> PathBuf {
        self.path.join(name)
    }
}

impl Drop for TempDir {
    fn drop(&mut self) {
        let _ = fs::remove_dir_all(&self.path);
    }
}

/// Create a minimal test PBL file with one source entry.
fn create_test_pbl(dir: &TempDir, name: &str) -> PathBuf {
    let pbl_path = dir.join(name);
    let mut writer = PblWriter::new(12, PblEncoding::Ansi);
    // obj_type: 6 = USEROBJECT; name must include extension (.sru)
    writer.add_entry("u_test_object.sru", 6, "// Test source\n// Generated by integration test".as_bytes().to_vec(), None);
    let bytes = writer.to_bytes().expect("PblWriter::to_bytes failed");
    let mut file = fs::File::create(&pbl_path).expect("Failed to create PBL file");
    file.write_all(&bytes).expect("Failed to write PBL bytes");
    pbl_path
}

/// Create a test PBL with multiple entries.
fn create_multi_pbl(dir: &TempDir, name: &str) -> PathBuf {
    let pbl_path = dir.join(name);
    let mut writer = PblWriter::new(12, PblEncoding::Ansi);
    // obj_type: 2=WINDOW(.srw), 1=DATAWINDOW(.srd), 4=FUNCTION(.srf)
    writer.add_entry("w_main.srw", 2, "// Main window".as_bytes().to_vec(), None);
    writer.add_entry("dw_customer.srd", 1, "// Customer grid".as_bytes().to_vec(), None);
    writer.add_entry("f_calc.srf", 4, "// Calculation function".as_bytes().to_vec(), None);
    let bytes = writer.to_bytes().expect("PblWriter::to_bytes failed");
    let mut file = fs::File::create(&pbl_path).expect("Failed to create PBL file");
    file.write_all(&bytes).expect("Failed to write PBL bytes");
    pbl_path
}

