// ── All command coverage test ──────────────────────────────────────────

#[test]
fn all_30_commands_are_routable() {
    let all_cmds: Vec<(&str, Vec<String>)> = vec![
        ("parse", vec!["/none".to_string()]),
        ("info", vec!["/none".to_string()]),
        ("list", vec!["/none".to_string()]),
        ("export", vec!["/none".to_string(), "obj".to_string()]),
        ("export-pbl", vec!["/none".to_string(), "/out".to_string()]),
        ("file-type", vec!["/none".to_string()]),
        ("analyze-pe", vec!["/none".to_string()]),
        ("extract-pbd", vec!["/none".to_string(), "/out".to_string()]),
        ("project", vec!["/none".to_string()]),
        ("find-pbl", vec!["/none".to_string()]),
        ("scan-export", vec!["/none".to_string(), "/out".to_string()]),
        ("pack-to-pbl", vec!["/none".to_string(), "/out".to_string()]),
        ("doctor", vec![]),
        ("search", vec!["/none".to_string(), "q".to_string()]),
        ("search-type", vec!["/none".to_string(), "Window".to_string()]),
        ("search-regex", vec!["/none".to_string(), ".*".to_string()]),
        ("analyze-dw", vec!["/none".to_string()]),
        ("dw-sql", vec!["/none".to_string()]),
        ("list-decompile", vec!["/none".to_string()]),
        ("decompile", vec!["/none".to_string(), "obj".to_string()]),
        ("decompile-all", vec!["/none".to_string(), "/out".to_string()]),
        ("report", vec!["/none".to_string()]),
        ("export-report", vec!["/none".to_string(), "/out".to_string()]),
        ("diff", vec!["/a".to_string(), "/b".to_string()]),
        ("workflow", vec!["/none".to_string()]),
        ("refactor", vec!["/none".to_string()]),
        ("snapshot", vec!["/none".to_string()]),
        ("review", vec!["/none".to_string()]),
        ("autoexport", vec!["/none".to_string()]),
        ("migrate", vec!["/none".to_string()]),
        ("build", vec!["/none".to_string(), "app".to_string()]),
    ];

    let routed = all_cmds.iter().filter(|(cmd, args)| {
        match execute_command(cmd, args) {
            Ok(_) => true,
            Err(e) => !e.contains("Unknown command"),
        }
    }).count();

    assert_eq!(routed, all_cmds.len(),
        "All {} commands must be routable, only {} routed",
        all_cmds.len(), routed);
}
